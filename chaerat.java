package Java;

public class chaerat {
	public static void main(String args[])
	{
		//CharAt()->returns char value in a index specified
		String str="Hello";
		char ch=str.charAt(3);
		System.out.println(ch);
		
		String a="Hello Programmer!";
		char b=a.charAt(6);
		System.out.println(b);
		
	}

}
